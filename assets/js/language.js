// Language Management
let currentLanguage = localStorage.getItem('language') || 'en';

function setLanguage(lang) {
    currentLanguage = lang;
    localStorage.setItem('language', lang);
    document.documentElement.lang = lang;
    updatePageContent();
}

function updatePageContent() {
    const elements = document.querySelectorAll('[data-translate]');
    elements.forEach(element => {
        const key = element.getAttribute('data-translate');
        if (translations[currentLanguage] && translations[currentLanguage][key]) {
            if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
                element.placeholder = translations[currentLanguage][key];
            } else if (element.tagName === 'BUTTON' || element.tagName === 'A') {
                // For buttons and links, update text content
                if (element.hasAttribute('data-html') || translations[currentLanguage][key].includes('<br>') || translations[currentLanguage][key].includes('<a')) {
                    element.innerHTML = translations[currentLanguage][key];
                } else {
                    element.textContent = translations[currentLanguage][key];
                }
            } else if (element.tagName === 'OPTION') {
                // For select options, update text content
                element.textContent = translations[currentLanguage][key];
            } else if (element.hasAttribute('data-html') || translations[currentLanguage][key].includes('<br>') || translations[currentLanguage][key].includes('<a')) {
                element.innerHTML = translations[currentLanguage][key];
            } else {
                element.textContent = translations[currentLanguage][key];
            }
        }
    });
    
    // Also update select option elements (in case they weren't caught above)
    const optionElements = document.querySelectorAll('option[data-translate]');
    optionElements.forEach(option => {
        const key = option.getAttribute('data-translate');
        if (translations[currentLanguage] && translations[currentLanguage][key]) {
            option.textContent = translations[currentLanguage][key];
        }
    });
    
    // Update language switcher
    const langSwitcher = document.querySelector('.language-switcher');
    if (langSwitcher) {
        const currentLangBtn = langSwitcher.querySelector('.current-lang');
        if (currentLangBtn) {
            currentLangBtn.textContent = currentLanguage.toUpperCase();
        }
    }
}

// Initialize language on page load
document.addEventListener('DOMContentLoaded', function() {
    setLanguage(currentLanguage);
    
    // Language switcher click handler
    const langSwitcher = document.querySelector('.language-switcher');
    if (langSwitcher) {
        langSwitcher.addEventListener('click', function() {
            const newLang = currentLanguage === 'en' ? 'fr' : 'en';
            setLanguage(newLang);
        });
    }
});

// Export for use in other scripts
window.setLanguage = setLanguage;
window.currentLanguage = () => currentLanguage;

